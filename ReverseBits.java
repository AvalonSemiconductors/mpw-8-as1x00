public class ReverseBits {
	public static void main(String[] args) {
		int[] rom_data = new int[] {
			0x0D, 0x44, 0x0D, 0x45,
			0x0D, 0x44, 0x0C, 0x40,
			0x0C, 0x49, 0x0C, 0x40,
			0x23, 0x4A, 0x02, 0x23,
			
			0x0A, 0x06, 0x40, 0x0A,
			0x01, 0x40, 0x0A, 0x05,
			0x40, 0x0A, 0x00, 0x0A,
			0x2D, 0x0A, 0x24, 0x05,
			
			0x0A, 0x23, 0x0A, 0x2F,
			0x0A, 0x0B, 0x08, 0x0A,
			0x0E, 0x0A, 0x07, 0x0A,
			0x24, 0x2B, 0x0D, 0x2C,
			
			0x2C, 0x0D, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00,
			0x00, 0x00, 0x00, 0x00,
		};
		
		for(int i = 0; i < rom_data.length; i++) {
			int x = rom_data[i];
			x = (x & 0xF0) >> 4 | (x & 0x0F) << 4;
			x = (x & 0xCC) >> 2 | (x & 0x33) << 2;
			x = (x & 0xAA) >> 1 | (x & 0x55) << 1;
			System.out.printf("0x%02X,", x);
			if((i + 1) % 4 == 0) System.out.println();
			else System.out.print(" ");
			if((i + 1) % 16 == 0) System.out.println();
		}
	}
}
